int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 0;
	j = 2;
	if (nb <= 0)
		 return (0);
	if (nb == 1)
		return (1);
	while (i < nb)
	{
		i = j * j;
		if (i > nb || j > 46341)
			return (0);
		j++;
	}
	return (j - 1);
}
