#include "ft.h"

void	ft_printtable(int **tab)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			ft_putchar(tab[i][j] + 48);
			ft_putchar(' ');
			j++;
		}
		write(1, "\n", 1);
		j = 0;
		i++;
	}
}
