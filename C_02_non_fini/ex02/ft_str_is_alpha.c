int	ft_str_is_alpha(char *str)
{
	char	a;

	a = 0;
	while (*str)
	{
		a = *str;
		if ((a < 65 || a > 90) && (a < 97 || a > 122))
			return (0);
		str++;
	}
	return (1);
}
