#include <unistd.h>
void	ft_putchar2(char n)
{
	write(1, &n, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar2(str[i]);
		i++;
	}
}
